package Area.Triangle.Rectangle;
// This is the superclass is written to calculate the area of rectangle.
/**
 * @author R.Santosh.
 *
 */
public class AreaRectangle {
	
	double dArea = 0;				//Declaration of variable.
	
	public void AreaRectangle(double dLength, double dBreadth)	//This is the method to calculate the area of rectangle.
	{
			
		dArea = dLength * dBreadth;				//This is the formula to calculate the area of rectangle.
				
	}

}
